/**
 * Provides the base implementation for the fvs active entry UAD anonymizer.
 *
 * <p>
 * This implementation exists only as a container for the default methods
 * generated by ServiceBuilder. All custom service methods should be put in
 * {@link FVSActiveEntryUADAnonymizer}.
 * </p>
 *
 * @author Brian Wing Shun Chan
 * @generated
 */
public abstract class BaseFVSActiveEntryUADAnonymizer
	extends DynamicQueryUADAnonymizer<FVSActiveEntry> {

	@Override
	public void autoAnonymize(
			FVSActiveEntry fvsActiveEntry, long userId, User anonymousUser)
		throws PortalException {

		if (fvsActiveEntry.getUserId() == userId) {
			fvsActiveEntry.setUserId(anonymousUser.getUserId());
			fvsActiveEntry.setUserName(anonymousUser.getFullName());
		}

		fvsActiveEntryLocalService.updateFVSActiveEntry(fvsActiveEntry);
	}

	@Override
	public void delete(FVSActiveEntry fvsActiveEntry) throws PortalException {
		fvsActiveEntryLocalService.deleteFVSActiveEntry(fvsActiveEntry);
	}

	@Override
	public Class<FVSActiveEntry> getTypeClass() {
		return FVSActiveEntry.class;
	}

	@Override
	protected ActionableDynamicQuery doGetActionableDynamicQuery() {
		return fvsActiveEntryLocalService.getActionableDynamicQuery();
	}

	@Override
	protected String[] doGetUserIdFieldNames() {
		return FVSUADConstants.USER_ID_FIELD_NAMES_FVS_ACTIVE_ENTRY;
	}

	@Reference
	protected FVSActiveEntryLocalService fvsActiveEntryLocalService;

}